# ---------------------------------------------------------------------------
# Главная структура: оборудование одного помещения
# ---------------------------------------------------------------------------


@dataclass
class RoomEquipment:
    """Конечное оборудование, установленное в помещении.

    Все поля опциональны. Пустые значения / "—" / 0 означают «не установлено».
    Программа использует эти данные ТОЛЬКО для отображения в сводной таблице
    (для проверки соответствия расчётной нагрузке) — на сам расчёт они не влияют.
    """

    # ----- Отопление -----
    heating_terminal_type: str = "—"     # см. HEATING_TERMINAL_TYPES
    heating_terminal_model: str = ""     # например "Kermi FKO 11 500x1000"
    heating_terminal_power_w: float = 0.0  # мощность ОДНОГО прибора, Вт
    heating_terminal_qty: int = 0        # количество приборов в помещении

    # ----- Охлаждение -----
    cooling_terminal_type: str = "—"
    cooling_terminal_model: str = ""     # например "Daikin FXFQ50A"
    cooling_terminal_power_w: float = 0.0  # холодопроизв. одного блока, Вт
    cooling_terminal_qty: int = 0

    # ----- Воздухораспределение: приток -----
    supply_terminal_type: str = "—"
    supply_terminal_model: str = ""      # например "АДН 300x150"
    supply_terminal_flow_m3h: float = 0.0  # расход через ОДИН диффузор, м³/ч
    supply_terminal_qty: int = 0

    # ----- Воздухораспределение: вытяжка -----
    exhaust_terminal_type: str = "—"
    exhaust_terminal_model: str = ""
    exhaust_terminal_flow_m3h: float = 0.0
    exhaust_terminal_qty: int = 0

    # ----- Дополнительно -----
    notes: str = ""                      # любые пометки инженера

    # ----- Производные значения (методы) -----

    @property
    def heating_total_w(self) -> float:
        """Суммарная мощность отопления в помещении."""
        return self.heating_terminal_power_w * max(self.heating_terminal_qty, 0)

    @property
    def cooling_total_w(self) -> float:
        """Суммарная мощность охлаждения в помещении."""
        return self.cooling_terminal_power_w * max(self.cooling_terminal_qty, 0)

    @property
    def supply_total_m3h(self) -> float:
        """Суммарный приток через все диффузоры помещения."""
        return self.supply_terminal_flow_m3h * max(self.supply_terminal_qty, 0)

    @property
    def exhaust_total_m3h(self) -> float:
        """Суммарная вытяжка через все решётки помещения."""
        return self.exhaust_terminal_flow_m3h * max(self.exhaust_terminal_qty, 0)

    @property
    def has_any(self) -> bool:
        """True если в помещении назначено хоть что-то."""
        return (self.heating_terminal_qty > 0 or self.cooling_terminal_qty > 0 or
                self.supply_terminal_qty > 0 or self.exhaust_terminal_qty > 0)

    def coverage_heating(self, design_q_w: float) -> float:
        """Покрытие расчётной теплопотери установленным оборудованием, %.
        Возвращает 0 если расчёт ещё не сделан."""
        if design_q_w <= 0:
            return 0.0
        return 100.0 * self.heating_total_w / design_q_w

    def coverage_cooling(self, design_q_w: float) -> float:
        """Покрытие расчётной теплопоступления установленным охлаждением, %."""
        if design_q_w <= 0:
            return 0.0
        return 100.0 * self.cooling_total_w / design_q_w


# ---------------------------------------------------------------------------
# Сериализация (для JSON save/load)
# ---------------------------------------------------------------------------


def serialize_room_equipment(eq: RoomEquipment) -> Dict:
    """RoomEquipment → dict. Чистые поля, без свойств."""
    return asdict(eq)


def deserialize_room_equipment(data: Dict) -> RoomEquipment:
    """dict → RoomEquipment. Защищён от устаревших полей."""
    if not data:
        return RoomEquipment()
    valid = {k: v for k, v in data.items()
             if k in RoomEquipment.__dataclass_fields__}
    return RoomEquipment(**valid)


