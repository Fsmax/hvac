"""Каталог конечного (terminal) оборудования, устанавливаемого в помещениях.

В отличие от hvac.equipment (это центральные системы — AHU, котлы, чиллеры),
здесь описывается то, что РЕАЛЬНО стоит В помещении:
- Отопительные приборы: радиаторы, конвекторы, фанкойлы, тёплые полы.
- Охлаждение: фанкойлы, кассеты VRF, сплиты, охлаждаемые балки.
- Вентиляционные решётки/диффузоры: приток и вытяжка.

Каждое помещение (Space) может содержать список таких устройств через поле
`Space.room_equipment` — это позволяет в финальной сводной таблице видеть
что именно установлено в каждой комнате и в каком количестве.
"""

