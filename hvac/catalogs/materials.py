def material_names_by_group() -> Dict[str, list[str]]:
    """Группировка имён для выпадающего списка в UI."""
    out: Dict[str, list[str]] = {}
    for m in MATERIALS.values():
        out.setdefault(m.group, []).append(m.name)
    for k in out:
        out[k].sort()
    return out


def get_material(name: str) -> Material | None:
    return MATERIALS.get(name)
