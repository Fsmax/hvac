# -*- coding: utf-8 -*-
"""Расчётные параметры микроклимата по климатическим зонам Узбекистана.

ШНҚ 2.08.02-23 «Жамоат бинолари ва иншоотлари», прил.8 табл.18 —
параметры воздуха в обслуживаемой зоне общественных зданий для тёплого
периода года по климатическим зонам I / II / III.

В каталоге зафиксированы ОПТИМАЛЬНЫЕ расчётные значения тёплого периода
(строка «О» табл.18): расчётная tв (°C), верхняя граница φ (%) и
предельная скорость воздуха (м/с). Дополнительно `t_permissible_max` —
допустимая верхняя tв без кондиционирования (строка «Н»).

Зона выбирается в проекте вручную (project.params.climate_zone), т.к.
географическое зонирование территории задаётся отдельной картой.
"""
from __future__ import annotations
from typing import Dict, List


# Зона → параметры тёплого периода (оптимальные, табл.18).
CLIMATE_ZONE_PARAMS: Dict[str, Dict] = {
    "I": {
        "t_cool": 26.0,            # расчётная tв тёплого периода, °C (О)
        "rh_max": 60,              # верхняя граница φ, %
        "v_max": 0.5,              # предельная скорость воздуха, м/с
        "t_permissible_max": 33.0, # допустимая tв без охлаждения (Н)
        "note": "ШНҚ 2.08.02-23 табл.18, зона I (жаркая)",
    },
    "II": {
        "t_cool": 25.0,
        "rh_max": 60,
        "v_max": 0.3,
        "t_permissible_max": 33.0,
        "note": "ШНҚ 2.08.02-23 табл.18, зона II",
    },
    "III": {
        "t_cool": 24.0,
        "rh_max": 60,
        "v_max": 0.2,
        "t_permissible_max": 28.0,
        "note": "ШНҚ 2.08.02-23 табл.18, зона III",
    },
}

DEFAULT_ZONE = "II"


def list_climate_zones() -> List[str]:
    """Список кодов зон в порядке I → III."""
    return list(CLIMATE_ZONE_PARAMS.keys())


def zone_params(zone: str) -> Dict:
    """Параметры микроклимата для зоны (фолбэк на зону по умолчанию)."""
    return CLIMATE_ZONE_PARAMS.get(zone, CLIMATE_ZONE_PARAMS[DEFAULT_ZONE])


def zone_indoor_cooling_temp(zone: str) -> float:
    """Рекомендуемая расчётная tв тёплого периода для зоны, °C."""
    return zone_params(zone)["t_cool"]
