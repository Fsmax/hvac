def get_norm_profile(params) -> Dict[str, str]:
    """Профиль подписей норм по ``params.thermal_norm``.

    ``KMK_UZ`` (дефолт проекта) → «UZ», иначе → «RU». Неизвестные значения
    трактуются как «RU» — прежнее поведение записки.
    """
    thermal = getattr(params, "thermal_norm", "") or ""
    if thermal.upper() == "KMK_UZ":
        return NORM_PROFILES["UZ"]
    return NORM_PROFILES["RU"]
